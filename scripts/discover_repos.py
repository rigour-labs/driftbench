def main():
    parser = argparse.ArgumentParser(description="Discover high-quality training repos via GitHub API")
    parser.add_argument("--min-score", type=int, default=40, help="Minimum score threshold (default: 40)")
    parser.add_argument("--max-candidates", type=int, default=15, help="Max repos to return (default: 15)")
    parser.add_argument("--since-days", type=int, default=90, help="Look back period in days (default: 90)")
    parser.add_argument("--track", choices=["ai", "enterprise", "both"], default="both",
                        help="Discovery track: ai (vibe-coded), enterprise (production), both (default)")
    parser.add_argument("--dry-run", action="store_true", help="Preview only, don't write files")
    parser.add_argument("--update-training", action="store_true", help="Also update repos_training.json")
    parser.add_argument("--repo-root", default=".", help="Path to driftbench repo root")
    args = parser.parse_args()

    candidates = run_discovery(
        repo_root=args.repo_root,
        min_score=args.min_score,
        max_candidates=args.max_candidates,
        since_days=args.since_days,
        dry_run=args.dry_run,
        track=args.track,
    )

    if not candidates:
        logger.info("No candidates found")
        return

    # Print summary table
    print("\n" + "=" * 70)
    print(f"{'Repo':<40} {'Score':>5} {'Stars':>7} {'Evidence'}")
    print("-" * 70)
    for c in candidates:
        evidence_short = "; ".join(c.get("evidence", [])[:2])
        print(f"{c['repo']:<40} {c['score']:>5} {c.get('stars', '?'):>7} {evidence_short[:40]}")
    print("=" * 70)

    if args.update_training and not args.dry_run:
        new_repos = [c["repo"] for c in candidates]
        update_training_repos(args.repo_root, new_repos)


if __name__ == "__main__":
    main()
