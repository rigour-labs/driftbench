def discover_from_coauthor_commits(token: str | None, since_days: int = 90) -> dict[str, list[str]]:
    """
    Search GitHub commits for AI co-author patterns.
    Returns {repo_full_name: [list of matching co-author strings]}.
    """
    since_date = (datetime.now(timezone.utc) - timedelta(days=since_days)).strftime("%Y-%m-%d")
    repo_signals: dict[str, list[str]] = {}

    for pattern in AI_COAUTHOR_PATTERNS:
        query = f'"{pattern}" committer-date:>{since_date}'
        logger.info(f"Searching commits: {pattern}")
        result = github_search("commits", query, token, per_page=30)
        time.sleep(SEARCH_DELAY)

        for item in result.get("items", []):
            repo = item.get("repository", {}).get("full_name", "")
            if repo:
                if repo not in repo_signals:
                    repo_signals[repo] = []
                repo_signals[repo].append(pattern)

    return repo_signals


def discover_from_topics(token: str | None, min_stars: int = 50, since_days: int = 90) -> set[str]:
    """Search repos by AI-related topics."""
    since_date = (datetime.now(timezone.utc) - timedelta(days=since_days)).strftime("%Y-%m-%d")
    repos = set()

    for topic in AI_TOPIC_TAGS:
        query = f"topic:{topic} stars:>{min_stars} pushed:>{since_date}"
        logger.info(f"Searching repos by topic: {topic}")
        result = github_search("repositories", query, token, per_page=20)
        time.sleep(SEARCH_DELAY)

        for item in result.get("items", []):
            repos.add(item["full_name"])

    return repos


def discover_from_cursorrules(token: str | None) -> set[str]:
    """Find repos that have .cursorrules file (strong Cursor signal)."""
    logger.info("Searching for repos with .cursorrules")
    query = "filename:.cursorrules path:/"
    result = github_search("code", query, token, per_page=30)
    time.sleep(SEARCH_DELAY)

    repos = set()
    for item in result.get("items", []):
        repo = item.get("repository", {}).get("full_name", "")
        if repo:
            repos.add(repo)

    return repos


def discover_enterprise_repos(token: str | None, since_days: int = 90) -> set[str]:
    """
    Discover production-grade repos across diverse enterprise domains.
    These repos have real drift patterns: staleness, security regressions,
    architecture violations — the patterns Rigour's customers actually face.
    """
    since_date = (datetime.now(timezone.utc) - timedelta(days=since_days)).strftime("%Y-%m-%d")
    repos = set()

    for query_template in ENTERPRISE_SEARCH_QUERIES:
        query = query_template.format(since_date=since_date)
        logger.info(f"Enterprise search: {query[:60]}...")
        result = github_search("repositories", query, token, per_page=10)
        time.sleep(SEARCH_DELAY)

        for item in result.get("items", []):
            repos.add(item["full_name"])

    logger.info(f"Enterprise track found {len(repos)} unique repos")
    return repos


def get_repo_quality_signals(owner_repo: str, token: str | None) -> dict:
    """
    Fetch code quality indicators that predict whether a repo produces
    good governance training data: CI/CD, tests, contributor count, size.
    """
    signals = {
        "has_ci": False,
        "has_tests": False,
        "contributor_count": 0,
        "age_days": 0,
    }

    # Check for CI/CD pipeline (GitHub Actions)
    url = f"{GITHUB_API}/repos/{owner_repo}/contents/.github/workflows"
    resp = requests.get(url, headers=get_headers(token), timeout=10)
    if resp.status_code == 200:
        workflows = resp.json()
        if isinstance(workflows, list) and len(workflows) > 0:
            signals["has_ci"] = True

    time.sleep(0.3)

    # Check for test directories
    for test_dir in ["tests", "test", "__tests__", "spec"]:
        url = f"{GITHUB_API}/repos/{owner_repo}/contents/{test_dir}"
        resp = requests.get(url, headers=get_headers(token), timeout=10)
        if resp.status_code == 200:
            signals["has_tests"] = True
            break
        time.sleep(0.2)

    # Get contributor count (first page — if 30+ contributors, that's a big team)
    url = f"{GITHUB_API}/repos/{owner_repo}/contributors"
    resp = requests.get(url, headers=get_headers(token), params={"per_page": 30}, timeout=10)
    if resp.status_code == 200:
        contributors = resp.json()
        if isinstance(contributors, list):
            signals["contributor_count"] = len(contributors)

    return signals


