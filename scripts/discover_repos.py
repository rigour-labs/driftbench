def score_repo(
    owner_repo: str,
    token: str | None,
    coauthor_signals: list[str] | None = None,
    has_cursorrules: bool = False,
    has_topic_match: bool = False,
    is_enterprise_track: bool = False,
) -> dict:
    """
    Score a repo on 0-100 scale based on training data quality potential.

    Scoring categories:
      A. AI-authorship signals (up to 40 pts) — vibe-coded repos
      B. Code maturity (up to 30 pts) — CI/CD, tests, contributors, age
      C. Production indicators (up to 30 pts) — stars, activity, language

    Both AI-signal and enterprise repos go through the same scorer.
    Enterprise repos score high on B+C even without A.
    """
    score = 0
    evidence = []

    # ── A. AI-Authorship Signals (up to 40 pts) ──────────────

    # 1. Co-Authored-By signals (strongest — up to 30 pts)
    if coauthor_signals:
        unique_patterns = len(set(coauthor_signals))
        total_signals = len(coauthor_signals)
        if total_signals >= 5:
            score += 30
            evidence.append(f"{total_signals} AI co-authored commits ({unique_patterns} distinct tools)")
        elif total_signals >= 1:
            score += 15
            evidence.append(f"{total_signals} AI co-authored commits")

    # 2. .cursorrules file (5 pts)
    if has_cursorrules:
        score += 5
        evidence.append("Has .cursorrules config")

    # 3. AI topic tags (5 pts)
    if has_topic_match:
        score += 5
        evidence.append("AI-related topic tags")

    # ── Fetch repo metadata ──────────────────────────────────

    repo_info = get_repo_info(owner_repo, token)
    if not repo_info:
        return {"repo": owner_repo, "score": 0, "evidence": ["ERROR: Could not fetch repo info"], "skip_reason": "api_error"}

    stars = repo_info.get("stargazers_count", 0)
    license_info = repo_info.get("license") or {}
    license_key = (license_info.get("spdx_id") or "").lower()
    language = repo_info.get("language") or ""
    topics = repo_info.get("topics", [])
    description = (repo_info.get("description") or "").lower()
    pushed_at = repo_info.get("pushed_at", "")
    created_at = repo_info.get("created_at", "")
    archived = repo_info.get("archived", False)
    fork = repo_info.get("fork", False)
    size_kb = repo_info.get("size", 0)  # repo size in KB

    # ── Skip conditions (harder filters) ─────────────────────

    if archived:
        return {"repo": owner_repo, "score": 0, "evidence": evidence, "skip_reason": "archived"}
    if fork:
        return {"repo": owner_repo, "score": 0, "evidence": evidence, "skip_reason": "fork"}
    # Raised from 50 → 200 stars. Weekend projects don't teach governance.
    if stars < 200:
        return {"repo": owner_repo, "score": 0, "evidence": evidence, "skip_reason": f"too_few_stars ({stars})"}
    if license_key and license_key not in ALLOWED_LICENSES and license_key != "noassertion":
        return {"repo": owner_repo, "score": 0, "evidence": evidence, "skip_reason": f"license ({license_key})"}
    # Skip tiny repos — not enough code to learn patterns from
    if size_kb < 500:  # less than 500KB total
        return {"repo": owner_repo, "score": 0, "evidence": evidence, "skip_reason": f"too_small ({size_kb}KB)"}

    # ── B. Code Maturity (up to 30 pts) ──────────────────────

    quality = get_repo_quality_signals(owner_repo, token)

    # 4. Has CI/CD pipeline (10 pts) — production-ready code
    if quality["has_ci"]:
        score += 10
        evidence.append("Has CI/CD pipeline")

    # 5. Has test suite (10 pts) — maintainable, testable code
    if quality["has_tests"]:
        score += 10
        evidence.append("Has test suite")

    # 6. Team-developed (up to 10 pts) — not a solo project
    contributors = quality["contributor_count"]
    if contributors >= 20:
        score += 10
        evidence.append(f"{contributors}+ contributors (large team)")
    elif contributors >= 10:
        score += 7
        evidence.append(f"{contributors} contributors")
    elif contributors >= 5:
        score += 4
        evidence.append(f"{contributors} contributors")

    # ── C. Production Indicators (up to 30 pts) ──────────────

    # 7. Stars (up to 10 pts)
    if stars >= 5000:
        score += 10
        evidence.append(f"{stars:,} stars (major project)")
    elif stars >= 1000:
        score += 7
        evidence.append(f"{stars:,} stars")
    elif stars >= 500:
        score += 5
        evidence.append(f"{stars:,} stars")
    else:
        score += 2
        evidence.append(f"{stars:,} stars")

    # 8. Active in last 30 days (5 pts)
    if pushed_at:
        try:
            pushed = datetime.fromisoformat(pushed_at.replace("Z", "+00:00"))
            if (datetime.now(timezone.utc) - pushed).days <= 30:
                score += 5
                evidence.append("Active in last 30 days")
        except ValueError:
            pass

    # 9. Mature project — created > 1 year ago (5 pts)
    if created_at:
        try:
            created = datetime.fromisoformat(created_at.replace("Z", "+00:00"))
            age_days = (datetime.now(timezone.utc) - created).days
            if age_days > 365:
                score += 5
                evidence.append(f"Mature ({age_days // 365}+ years old)")
        except ValueError:
            pass

    # 10. TypeScript, Python, Go, Rust (5 pts) — languages Rigour supports
    supported_languages = {"typescript", "python", "javascript", "go", "rust", "java"}
    if language.lower() in supported_languages:
        score += 5
        evidence.append(f"Primary language: {language}")

    # 11. Check commit messages for AI patterns (5 pts) — bonus for AI-signal track
    if not is_enterprise_track:
        commits = get_repo_commits(owner_repo, token, since_days=90, per_page=50)
        ai_commit_count = 0
        for commit in commits:
            msg = (commit.get("commit", {}).get("message") or "").lower()
            if any(p in msg for p in AI_COMMIT_MESSAGE_PATTERNS):
                ai_commit_count += 1
        if ai_commit_count > 0:
            score += 5
            evidence.append(f"{ai_commit_count} commits with AI-related messages")

    # Track which discovery track found this repo
    track = "enterprise" if is_enterprise_track else "ai-signal"

    return {
        "repo": owner_repo,
        "score": score,
        "stars": stars,
        "language": language,
        "license": license_key or "unknown",
        "evidence": evidence,
        "topics": topics[:10],
        "track": track,
        "quality": {
            "has_ci": quality["has_ci"],
            "has_tests": quality["has_tests"],
            "contributors": quality["contributor_count"],
        },
    }


