def load_existing_repos(repo_root: str) -> tuple[set[str], set[str]]:
    """Load training + eval repo lists to avoid duplicates."""
    training_file = os.path.join(repo_root, "rlaif", "repos_training.json")
    training_repos = set()
    eval_repos = set()

    if os.path.exists(training_file):
        with open(training_file) as f:
            data = json.load(f)
        training_repos = set(data.get("repos", []))
        eval_repos = set(data.get("_eval_repos_DO_NOT_ADD", []))

    return training_repos, eval_repos


def run_discovery(
    repo_root: str,
    min_score: int = 40,
    max_candidates: int = 15,
    since_days: int = 90,
    dry_run: bool = False,
    track: str = "both",
) -> list[dict]:
    """
    Full discovery pipeline with two tracks:
      A. AI-signal: repos with AI co-authorship (vibe-coded)
      B. Enterprise: mature production repos across diverse domains

    Both tracks go through the same quality scorer.
    Final list balances AI-signal and enterprise repos.
    """
    token = os.environ.get("GITHUB_TOKEN") or os.environ.get("GH_TOKEN")
    if not token:
        logger.warning("No GITHUB_TOKEN set — API rate limits will be very low (10 searches/min)")

    # Load existing repos to skip
    training_repos, eval_repos = load_existing_repos(repo_root)
    blocklist = training_repos | eval_repos
    logger.info(f"Blocklist: {len(blocklist)} repos ({len(training_repos)} training, {len(eval_repos)} eval)")

    # ── Track A: AI-signal discovery ─────────────────────────
    ai_candidates: set[str] = set()
    coauthor_repos: dict[str, list[str]] = {}
    topic_repos: set[str] = set()
    cursorrules_repos: set[str] = set()

    if track in ("both", "ai"):
        logger.info("=== Track A: AI-Signal Discovery ===")
        coauthor_repos = discover_from_coauthor_commits(token, since_days)
        topic_repos = discover_from_topics(token, min_stars=200, since_days=since_days)
        cursorrules_repos = discover_from_cursorrules(token)
        ai_candidates = set(coauthor_repos.keys()) | topic_repos | cursorrules_repos
        ai_candidates -= blocklist
        logger.info(f"AI track: {len(ai_candidates)} candidates")
        logger.info(f"  - Co-author signal: {len(coauthor_repos)} repos")
        logger.info(f"  - Topic match: {len(topic_repos)} repos")
        logger.info(f"  - .cursorrules: {len(cursorrules_repos)} repos")

    # ── Track B: Enterprise discovery ────────────────────────
    enterprise_candidates: set[str] = set()

    if track in ("both", "enterprise"):
        logger.info("=== Track B: Enterprise Discovery ===")
        enterprise_candidates = discover_enterprise_repos(token, since_days)
        enterprise_candidates -= blocklist
        enterprise_candidates -= ai_candidates  # No overlap
        logger.info(f"Enterprise track: {len(enterprise_candidates)} candidates")

    all_candidates = ai_candidates | enterprise_candidates
    logger.info(f"Total unique candidates: {len(all_candidates)}")

    # ── Score all candidates ─────────────────────────────────
    logger.info("=== Scoring candidates ===")
    scored = []
    for i, repo in enumerate(sorted(all_candidates)):
        is_enterprise = repo in enterprise_candidates
        logger.info(f"  Scoring [{i+1}/{len(all_candidates)}] ({'enterprise' if is_enterprise else 'ai-signal'}): {repo}")
        result = score_repo(
            repo,
            token,
            coauthor_signals=coauthor_repos.get(repo),
            has_cursorrules=(repo in cursorrules_repos),
            has_topic_match=(repo in topic_repos),
            is_enterprise_track=is_enterprise,
        )
        scored.append(result)
        time.sleep(0.5)  # Be kind to the API

    # ── Filter and rank with diversity ───────────────────────
    logger.info("=== Filtering and ranking ===")
    qualified = [r for r in scored if r.get("score", 0) >= min_score and "skip_reason" not in r]
    qualified.sort(key=lambda x: x["score"], reverse=True)

    # Ensure diversity: at least 40% from each active track
    if track == "both" and len(qualified) > 4:
        ai_qualified = [r for r in qualified if r.get("track") == "ai-signal"]
        ent_qualified = [r for r in qualified if r.get("track") == "enterprise"]
        min_per_track = max(2, max_candidates * 2 // 5)  # 40% floor

        candidates = []
        # Take top from each track first
        candidates.extend(ai_qualified[:min_per_track])
        candidates.extend(ent_qualified[:min_per_track])
        # Fill remaining with highest-scored from either track
        remaining_pool = [r for r in qualified if r not in candidates]
        remaining_pool.sort(key=lambda x: x["score"], reverse=True)
        candidates.extend(remaining_pool[:max(0, max_candidates - len(candidates))])
        # Re-sort final list by score
        candidates.sort(key=lambda x: x["score"], reverse=True)
        candidates = candidates[:max_candidates]
    else:
        candidates = qualified[:max_candidates]

    skipped = [r for r in scored if "skip_reason" in r]
    below_threshold = [r for r in scored if r.get("score", 0) < min_score and "skip_reason" not in r]

    logger.info(f"Results: {len(candidates)} candidates (score >= {min_score})")
    ai_count = sum(1 for c in candidates if c.get("track") == "ai-signal")
    ent_count = sum(1 for c in candidates if c.get("track") == "enterprise")
    logger.info(f"  AI-signal: {ai_count} | Enterprise: {ent_count}")
    logger.info(f"  Skipped: {len(skipped)} (archived/fork/license/stars/size)")
    logger.info(f"  Below threshold: {len(below_threshold)}")

    for c in candidates:
        ci_flag = "✓CI" if c.get("quality", {}).get("has_ci") else ""
        test_flag = "✓Tests" if c.get("quality", {}).get("has_tests") else ""
        logger.info(f"  ✓ {c['repo']} (score={c['score']}, stars={c.get('stars', '?')}, {c.get('track', '?')}) {ci_flag} {test_flag}")

    # ── Output ───────────────────────────────────────────────
    if not dry_run:
        output_dir = os.path.join(repo_root, "results")
        os.makedirs(output_dir, exist_ok=True)
        output_path = os.path.join(output_dir, "candidates.json")
        output = {
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "discovery_params": {
                "min_score": min_score,
                "max_candidates": max_candidates,
                "since_days": since_days,
                "track": track,
            },
            "summary": {
                "total_searched": len(all_candidates),
                "qualified": len(qualified),
                "returned": len(candidates),
                "skipped": len(skipped),
                "below_threshold": len(below_threshold),
                "track_breakdown": {
                    "ai_signal": ai_count,
                    "enterprise": ent_count,
                },
            },
            "candidates": candidates,
            "skipped": skipped[:20],
        }
        with open(output_path, "w") as f:
            json.dump(output, f, indent=2)
        logger.info(f"Wrote {output_path}")

    return candidates


def update_training_repos(repo_root: str, new_repos: list[str]) -> bool:
    """Add new repos to repos_training.json. Returns True if modified."""
    training_file = os.path.join(repo_root, "rlaif", "repos_training.json")
    with open(training_file) as f:
        data = json.load(f)

    existing = set(data.get("repos", []))
    to_add = [r for r in new_repos if r not in existing]

    if not to_add:
        logger.info("No new repos to add")
        return False

    data["repos"].extend(to_add)

    # Update categories comment
    categories = data.get("_categories", {})
    discovered_list = ", ".join(to_add)
    categories["auto_discovered"] = f"{discovered_list} ({len(to_add)})"
    data["_categories"] = categories

    with open(training_file, "w") as f:
        json.dump(data, f, indent=2)
        f.write("\n")

    logger.info(f"Added {len(to_add)} repos to {training_file}")
    return True


